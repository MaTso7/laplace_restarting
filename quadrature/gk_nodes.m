function [nodes15, weights15, weights7] = gk_nodes(a,b)
% returns the Gauss-Kronrod 7/15 nodes and weights for the interval [a,b]
%
% uses the nodes and weights for [-1, 1] computed in quadruple precision as described in
% Gautschi: "Orthogonal Polynomials: Computation and Approximation", 2004, Sec. 3.1.2.
% compare with https://www.advanpix.com/2011/11/07/gauss-kronrod-quadrature-nodes-weights/

nodes15 = [  0, ... %G
             2.077849550078984676006894037732449e-01, ...
             4.058451513773971669066064120769615e-01, ... %G
             5.860872354676911302941448382587296e-01, ...
             7.415311855993944398638647732807884e-01, ... %G
             8.648644233597690727897127886409262e-01, ...
             9.491079123427585245261896840478513e-01, ... %G
             9.914553711208126392068546975263285e-01, ...
            ];
weights15 = [ 2.094821410847278280129991748917143e-01, ...
              2.044329400752988924141619992346491e-01, ...
              1.903505780647854099132564024210137e-01, ...
              1.690047266392679028265834265985503e-01, ...
              1.406532597155259187451895905102379e-01, ...
              1.047900103222501838398763225415180e-01, ...
              6.309209262997855329070066318920429e-02, ...
              2.293532201052922496373200805896959e-02, ...
            ];
weights7 =  [ 4.179591836734693877551020408163265e-01, ...
              0, ...
              3.818300505051189449503697754889751e-01, ...
              0, ...
              2.797053914892766679014677714237796e-01, ...
              0, ...
              1.294849661688696932706114326790820e-01, ...
              0, ...
            ];
nodes15   = [nodes15,  -nodes15(2:end)];
weights15 = [weights15, weights15(2:end)];
weights7  = [weights7,  weights7(2:end)];

nodes15   = nodes15   *(b-a)/2 + (a+b)/2;
nodes15   = transpose(nodes15);
weights15 = weights15 *(b-a)/2;
weights7  = weights7  *(b-a)/2;
end